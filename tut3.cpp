#include <iostream>
using namespace std;

int main() {
  cout << "Operators in c++" <<endl;
  
  // Assignment operator
  int a=4, b=5;

  // Arithmetic Operator 
  cout << "a + b is: " << (a+b) <<endl;
  cout << "a - b is: " << (a-b) <<endl;
  cout << "a * b is: " << (a*b) <<endl;
  cout << "a / b is: " << (a/b) <<endl;
  cout << "a % b is: " << (a%b) <<endl;
  cout << "a++ is: " << (a++) <<endl;
  cout << "a-- is: " << (a--) <<endl;
  cout << "++a is: " << (++a) <<endl;
  cout << "--a is: " << (--a) <<endl;

  // comparision operator 
  
  // logical operator



  return 0;
}
